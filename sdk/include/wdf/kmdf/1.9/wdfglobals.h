#ifndef _WDFGLOBALS_H_
#define _WDFGLOBALS_H_
 
#define WDF_DRIVER_GLOBALS_NAME_LEN (32)

typedef struct _WDF_DRIVER_GLOBALS
{
    /* Handle backpointer */
    WDFDRIVER Driver;
	
    /* Flags used by driver during creation */
    ULONG DriverFlags;
	
    /* Generated by WDF, used during driver allocations */
    ULONG DriverTag;

    CHAR DriverName[WDF_DRIVER_GLOBALS_NAME_LEN];
	
    /* When true, stub code will capture DriverObject->DriverUnload and insert
	itself in the unload chain. */
    BOOLEAN DisplaceDriverUnload;
} WDF_DRIVER_GLOBALS, *PWDF_DRIVER_GLOBALS;

#endif /* _WDFGLOBALS_H_ */
